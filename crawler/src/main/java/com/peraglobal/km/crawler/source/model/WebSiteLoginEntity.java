package com.peraglobal.km.crawler.source.model;

public class WebSiteLoginEntity {
	private String loginRequest;
	private String user_k;
	private String password_k;
	private String username;
	private String password;
	private String loginSubmit;
	private String indexUrl;
	public String getLoginRequest() {
		return loginRequest;
	}
	public void setLoginRequest(String loginRequest) {
		this.loginRequest = loginRequest;
	}
	public String getUser_k() {
		return user_k;
	}
	public void setUser_k(String user_k) {
		this.user_k = user_k;
	}
	public String getPassword_k() {
		return password_k;
	}
	public void setPassword_k(String password_k) {
		this.password_k = password_k;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getLoginSubmit() {
		return loginSubmit;
	}
	public void setLoginSubmit(String loginSubmit) {
		this.loginSubmit = loginSubmit;
	}
	public String getIndexUrl() {
		return indexUrl;
	}
	public void setIndexUrl(String indexUrl) {
		this.indexUrl = indexUrl;
	}
	
}
